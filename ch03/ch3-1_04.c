#include <stdio.h>
int main(void) {
    unsigned int a = 60; // 60 = 0011 1100
    unsigned int b = 13; // 13 = 0000 1101
    int c = 0;

    c = a & b; //12 = 0000 1100
    printf("c = %d\n", c);

    c = a | b; //61 = 0011 1101
    printf("c = %d\n", c);

    c = a ^ b; //12 = 0011 0001
    printf("c = %d\n", c);

    c = ~a; // -61 = 1100 0011
    printf("c = %d\n", c);

    // a = 60 (이진수: 0011 1100) 이라고 가정

    c = a << 2;
    // [왼쪽 시프트] 비트를 왼쪽으로 2칸 이동: 1111 0000 (십진수 240)
    // 원리: 왼쪽으로 n칸 밀면 값에 2^n을 곱한 것과 같음 (60 * 2^2 = 240)
    // 특징: 곱셈 연산보다 속도가 빨라 시스템 최적화 시 자주 사용됨
    printf("c = %d\n", c);

    c = a >> 2;
    // [오른쪽 시프트] 비트를 오른쪽으로 2칸 이동: 0000 1111 (십진수 15)
    // 원리: 오른쪽으로 n칸 밀면 값을 2^n으로 나눈 것과 같음 (60 / 2^2 = 15)
    // 특징: 나눗셈 연산(특히 정수 나눗셈)의 최적화에 유리함
    printf("c = %d\n", c);

    return 0;
}